    public function signIn(Request $request)
    {
        $user = Auth::attempt([
            'username' => $request->username,
            'password' => $request->password
        ]);

        if ($user) {
            return Auth::user();
        } else {
            return response([
                'error' => 'Tên đăng nhập hoặc mật khẩu sai',
            ], 200);
        }
    }

    public function logOut()
